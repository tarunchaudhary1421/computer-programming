'''3. How to add a new key-value pair to a dictionary?
Question: How can you add a new key "email" with value "john@example.com"?'''
person = {
    "name": "John",
    "age": 30,
    "city": "New York"
}
# Adding a new key-value pair to the dictionary
person["email"] = "john@example.com"
print(person)
